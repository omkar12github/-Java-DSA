public class ASCIIValues {
    public static void main(String[] args) {
        for (int  i = 0; i <= 127; i++){
              System.out.println(i + " = " + (char) i);
        }
    }
}


// ASCII stands for American Standard Code for Information Interchange. 
// It is a character encoding system used in computers to represent letters, numbers, symbols, and control characters
//  using numeric values.
// In ASCII, each character is assigned a unique number between 0 and 127. For example:

// A = 65
// a = 97
// 0 = 48

// ASCII helps computers store and process text data because computers understand numbers, not characters directly.

// The ASCII table includes:

// Alphabets (A-Z, a-z)
// Digits (0-9)
// Special symbols (@, #, $)
// Control characters (newline, tab, etc.)

// ASCII is widely used in programming, data communication, and text processing.