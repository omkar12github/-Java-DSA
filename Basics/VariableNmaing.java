public class VariableNmaing {
    public static void main(String[] args) {
        int sumit  = 100;
        int guddu = 200;
        System.out.println(sumit + guddu);
    }
}


// Variable naming rules in Java:
// 1. Variable names can only contain letters, digits, underscores, and dollar signs.
// 2. Variable names must begin with a letter, an underscore, or a dollar sign
// 3. Variable names are case-sensitive.
// 4. Variable names cannot be a reserved keyword in Java (like int, class, etc.)
// from keyboard characters like !, @, #, etc. are not allowed in variable names. only _ and $ are allowed.